    // ───────────────────────────── Function execution ─────────────────────────────

    private Map<String, Object> executeFunction(String intentType, Map<String, Object> intent, User user,
                                                 Map<String, Object> sessionContext, String userMessage,
                                                 List<Map<String, String>> conversationHistory) {
        Map<String, Object> result = new LinkedHashMap<>();
        Integer limit = intent.get("limit") instanceof Number n ? n.intValue() : 5;

        switch (intentType) {
            case "SEARCH_EVENTS" -> {
                String keyword = (String) intent.get("keyword");
                String category = (String) intent.get("category");
                String city = (String) intent.get("city");
                String venue = (String) intent.get("venue");

                List<Event> events;
                if (venue != null && !venue.isBlank()) {
                    // Venue / address search — cover both the venue name and the full address.
                    events = eventRepository.searchEventsByKeyword(
                            venue, LocalDateTime.now(), PageRequest.of(0, limit));
                    result.put("searchMode", "venue");
                    result.put("searchTerm", venue);
                } else if (keyword != null && !keyword.isBlank()) {
                    events = eventRepository.searchEventsByKeyword(
                            keyword, LocalDateTime.now(), PageRequest.of(0, limit));
                    result.put("searchMode", "keyword");
                    result.put("searchTerm", keyword);
                } else if (city != null && !city.isBlank()) {
                    events = cityRepository.findByNameContainingIgnoreCase(city)
                            .stream()
                            .findFirst()
                            .map(c -> eventRepository.findUpcomingEventsByCity(
                                    c, LocalDateTime.now(), LocalDateTime.now().plusMonths(3),
                                    PageRequest.of(0, limit)).getContent())
                            .orElseGet(() -> eventRepository.searchEventsByKeyword(
                                    city, LocalDateTime.now(), PageRequest.of(0, limit)));
                    result.put("searchMode", "city");
                    result.put("searchTerm", city);
                } else if (category != null && !category.isBlank()) {
                    events = categoryRepository.findByNameContainingIgnoreCase(category)
                            .stream()
                            .findFirst()
                            .map(c -> eventRepository.findUpcomingEventsByCategory(
                                    c, LocalDateTime.now(),
                                    PageRequest.of(0, limit)).getContent())
                            .orElseGet(() -> eventRepository.searchEventsByKeyword(
                                    category, LocalDateTime.now(), PageRequest.of(0, limit)));
                    result.put("searchMode", "category");
                    result.put("searchTerm", category);
                } else {
                    events = eventRepository.findUpcomingPublicEvents(
                            LocalDateTime.now(), LocalDateTime.now().plusMonths(3),
                            PageRequest.of(0, limit)).getContent();
                    result.put("searchMode", "recent");
                }

                result.put("events", summarizeEvents(events));
                result.put("count", events.size());
            }

            case "EVENT_DETAILS" -> {
                String keyword = (String) intent.get("keyword");
                List<Event> matches = (keyword != null && !keyword.isBlank())
                        ? eventRepository.searchEventsByKeyword(
                                keyword, LocalDateTime.now(), PageRequest.of(0, 3))
                        : List.of();
                if (!matches.isEmpty()) {
                    result.put("event", detailedEvent(matches.get(0)));
                    if (matches.size() > 1) {
                        result.put("otherMatches", summarizeEvents(matches.subList(1, matches.size())));
                    }
                    result.put("count", 1);
                } else {
                    result.put("event", null);
                    result.put("count", 0);
                    result.put("searchTerm", keyword);
                }
            }

            case "COMPARE_EVENTS" -> {
                @SuppressWarnings("unchecked")
                List<String> titles = intent.get("eventTitles") instanceof List<?> raw
                        ? raw.stream().filter(Objects::nonNull).map(Object::toString).toList()
                        : List.of();
                List<Map<String, Object>> resolved = new ArrayList<>();
                List<String> notFound = new ArrayList<>();
                for (String title : titles) {
                    if (title == null || title.isBlank()) continue;
                    List<Event> match = eventRepository.searchEventsByKeyword(
                            title, LocalDateTime.now(), PageRequest.of(0, 1));
                    if (!match.isEmpty()) {
                        resolved.add(detailedEvent(match.get(0)));
                    } else {
                        notFound.add(title);
                    }
                }
                result.put("events", resolved);
                result.put("count", resolved.size());
                if (!notFound.isEmpty()) result.put("notFound", notFound);
            }

            case "REGISTRATION_HELP" -> {
                // Include the user's upcoming tickets so the assistant can answer
                // "cancel my ticket" / "my check-in code" kinds of questions.
                if (user != null) {
                    List<Event> myEvents = registrationRepository
                            .findUpcomingRegistrationsByUser(user, PageRequest.of(0, 5))
                            .stream().map(Registration::getEvent).toList();
                    result.put("upcomingTickets", summarizeEvents(myEvents));
                }
                result.put("helpTopic", "registration");
            }

            case "SEARCH_BY_SPEAKER" -> {
                String speaker = (String) intent.get("speaker");
                List<Event> events = (speaker != null && !speaker.isBlank())
                        ? eventRepository.findEventsBySpeakerName(speaker, PageRequest.of(0, limit)).getContent()
                        : List.of();
                result.put("events", summarizeEvents(events));
                result.put("count", events.size());
                result.put("speaker", speaker);
            }

            case "UPCOMING_EVENTS" -> {
                List<Event> events = eventRepository.findUpcomingPublicEvents(
                        LocalDateTime.now(),
                        LocalDateTime.now().plusDays(14),
                        PageRequest.of(0, limit)).getContent();
                result.put("events", summarizeEvents(events));
                result.put("count", events.size());
            }

            case "RECOMMEND_EVENTS" -> {
                List<Event> events = new ArrayList<>(eventRepository.findUpcomingPublicEvents(
                        LocalDateTime.now(),
                        LocalDateTime.now().plusMonths(2),
                        PageRequest.of(0, limit * 3)).getContent());

                // Personalized re-rank: boost events whose category matches user's
                // interests OR the user's most-registered categories.
                Set<String> preferred = preferredCategories(user);
                events.sort((a, b) -> {
                    int popularityCmp = Integer.compare(b.getApprovedCount(), a.getApprovedCount());
                    if (preferred.isEmpty()) return popularityCmp;
                    boolean aPref = a.getCategory() != null && preferred.contains(a.getCategory().getName().toLowerCase());
                    boolean bPref = b.getCategory() != null && preferred.contains(b.getCategory().getName().toLowerCase());
                    if (aPref != bPref) return aPref ? -1 : 1;
                    return popularityCmp;
                });

                List<Event> top = events.stream().limit(limit).toList();
                result.put("events", summarizeEvents(top));
                result.put("count", top.size());
                result.put("strategy", preferred.isEmpty()
                        ? "Most popular upcoming events"
                        : "Personalized: re-ranked by your interests");
            }

            case "LIST_CATEGORIES" -> {
                List<Map<String, Object>> cats = categoryRepository.findAll().stream()
                        .limit(20)
                        .map(c -> {
                            Map<String, Object> m = new LinkedHashMap<>();
                            m.put("name", c.getName());
                            m.put("description", c.getDescription());
                            return m;
                        })
                        .toList();
                result.put("categories", cats);
                result.put("count", cats.size());
            }

            case "LIST_CITIES" -> {
                List<Map<String, Object>> cities = cityRepository.findAll().stream()
                        .limit(20)
                        .map(c -> {
                            Map<String, Object> m = new LinkedHashMap<>();
                            m.put("name", c.getName());
                            m.put("country", c.getCountry());
                            return m;
                        })
                        .toList();
                result.put("cities", cities);
                result.put("count", cities.size());
            }

            case "EVENT_PRICE_QUERY" -> {
                List<Event> events = eventRepository.findUpcomingPublicEvents(
                        LocalDateTime.now(), LocalDateTime.now().plusMonths(3),
                        PageRequest.of(0, 50)).getContent();
                java.math.BigDecimal min = null, max = null, total = java.math.BigDecimal.ZERO;
                int count = 0;
                for (Event e : events) {
                    if (e.getTicketPrice() != null && e.getTicketPrice().compareTo(java.math.BigDecimal.ZERO) > 0) {
                        if (min == null || e.getTicketPrice().compareTo(min) < 0) min = e.getTicketPrice();
                        if (max == null || e.getTicketPrice().compareTo(max) > 0) max = e.getTicketPrice();
                        total = total.add(e.getTicketPrice());
                        count++;
                    }
                }
                result.put("priceRange", Map.of(
                        "min", min != null ? min : 0,
                        "max", max != null ? max : 0,
                        "average", count > 0 ? total.divide(java.math.BigDecimal.valueOf(count), 2, java.math.RoundingMode.HALF_UP) : 0,
                        "samplesAnalyzed", count
                ));
            }

            case "BOOK_TICKET" -> {
                String keyword = (String) intent.get("keyword");
                Event target = resolveEventForBooking(keyword, sessionContext);

                if (target == null) {
                    result.put("needEventSelection", true);
                    List<Event> upcoming = eventRepository.findUpcomingPublicEvents(
                            LocalDateTime.now(), LocalDateTime.now().plusMonths(3),
                            PageRequest.of(0, 5)).getContent();
                    result.put("events", summarizeEvents(upcoming));
                    break;
                }

                result.put("event", detailedEvent(target));

                if (user == null) {
                    result.put("requiresAuth", true);
                    break;
                }

                // Already registered? Surface the existing ticket instead of creating a duplicate.
                var existing = registrationRepository.findByUserAndEvent(user, target);
                if (existing.isPresent()) {
                    result.put("alreadyRegistered", true);
                    result.put("existingTicket", summarizeTicket(existing.get()));
                    result.put("bookingAction", "VIEW_EXISTING");
                    break;
                }

                List<com.luma.entity.TicketType> ticketTypes =
                        ticketTypeRepository.findAvailableByEventId(target.getId());
                result.put("ticketTypes", summarizeTicketTypes(ticketTypes));
                result.put("ticketTypeCount", ticketTypes.size());

                boolean isFree = target.isFree() ||
                        (target.getTicketPrice() == null ||
                         target.getTicketPrice().compareTo(java.math.BigDecimal.ZERO) == 0);

                // Does the organiser require registration form answers?
                boolean hasForm = !registrationQuestionRepository
                        .findByEventIdOrderByDisplayOrderAsc(target.getId()).isEmpty();
                boolean hasMultipleTicketTypes = ticketTypes.size() > 1;

                // Route to the right flow. The mobile app has 3 screens:
                //   1. Registration form (questions + optional ticket picker)
                //   2. Stripe checkout (paid events, pays after form if any)
                //   3. Ticket detail with QR (after successful registration)
                result.put("eventId", target.getId());
                result.put("eventTitle", target.getTitle());
                result.put("isFree", isFree);
                result.put("hasRegistrationForm", hasForm);
                result.put("hasMultipleTicketTypes", hasMultipleTicketTypes);

                if (!isFree) {
                    // Paid event — mobile opens form (if any) then Stripe checkout.
                    // Never auto-charge from the chatbot; user must confirm payment.
                    result.put("registered", false);
                    result.put("bookingAction", hasForm ? "OPEN_FORM_THEN_CHECKOUT" : "OPEN_CHECKOUT");
                    result.put("actionLabel", hasForm ? "Điền form & thanh toán" : "Thanh toán ngay");
                } else if (hasForm || hasMultipleTicketTypes) {
                    // Free event but needs user input (form answers or ticket pick)
                    // → mobile opens registration form screen, no auto-register.
                    result.put("registered", false);
                    result.put("bookingAction", "OPEN_FORM");
                    result.put("actionLabel", hasForm
                            ? "Điền form đăng ký"
                            : "Chọn loại vé");
                } else {
                    // Free + no form + single ticket type → safe to auto-register.
                    try {
                        UUID ticketTypeId = ticketTypes.isEmpty() ? null : ticketTypes.get(0).getId();
                        com.luma.dto.response.RegistrationResponse reg =
                                registrationService.registerForEvent(user, target.getId(), ticketTypeId, 1);
                        result.put("registered", true);
                        result.put("registration", reg);
                        result.put("bookingAction", "VIEW_TICKET");
                        result.put("actionLabel", "Xem vé & QR");
                    } catch (Exception e) {
                        log.warn("Free auto-registration failed for event {}: {}", target.getId(), e.getMessage());
                        result.put("registered", false);
                        result.put("bookingError", e.getMessage());
                        result.put("bookingAction", "OPEN_EVENT_PAGE");
                        result.put("actionLabel", "Mở trang sự kiện");
                    }
                }
            }

            case "CHECK_PAYMENT" -> {
                if (user == null) {
                    result.put("requiresAuth", true);
                    break;
                }
                UUID registrationId = parseUuid(intent.get("registrationId"));
                if (registrationId == null) registrationId = parseUuid(sessionContextGet(sessionContext, "activeRegistrationId"));

                if (registrationId != null) {
                    try {
                        result.put("payment", paymentService.getPaymentByRegistrationId(registrationId, user));
                        result.put("count", 1);
                    } catch (Exception e) {
                        log.warn("Failed to load payment for registration {}: {}", registrationId, e.getMessage());
                        result.put("payment", null);
                        result.put("error", e.getMessage());
                    }
                } else {
                    // Fall back to the user's 3 most recent registrations so they can pick.
                    List<Registration> recent = registrationRepository
                            .findByUserOrderByCreatedAtDesc(user, PageRequest.of(0, 3))
                            .getContent();
                    List<Map<String, Object>> summaries = recent.stream()
                            .map(this::summarizeRegistrationPayment)
                            .toList();
                    result.put("registrations", summaries);
                    result.put("count", summaries.size());
                }
            }

            case "TICKET_QR" -> {
                if (user == null) {
                    result.put("requiresAuth", true);
                    break;
                }
                List<Registration> approved = registrationRepository
                        .findUpcomingRegistrationsByUser(user, PageRequest.of(0, 10))
                        .stream()
                        .filter(r -> r.getTicketCode() != null && !r.getTicketCode().isBlank())
                        .toList();
                List<Map<String, Object>> tickets = approved.stream()
                        .map(this::summarizeTicket)
                        .toList();
                result.put("tickets", tickets);
                result.put("count", tickets.size());
            }

            case "CANCEL_REGISTRATION" -> {
                if (user == null) {
                    result.put("requiresAuth", true);
                    break;
                }
                UUID registrationId = parseUuid(intent.get("registrationId"));
                if (registrationId == null) registrationId = parseUuid(sessionContextGet(sessionContext, "activeRegistrationId"));

                if (registrationId != null) {
                    // User has already picked which ticket — expose it for the UI
                    // confirmation card. Actual cancellation still goes through
                    // the existing DELETE endpoint (two-step flow).
                    Registration reg = registrationRepository.findById(registrationId).orElse(null);
                    if (reg != null && reg.getUser() != null && reg.getUser().getId().equals(user.getId())) {
                        result.put("registration", summarizeTicket(reg));
                        result.put("cancellable", reg.getStatus() != RegistrationStatus.CANCELLED
                                && (reg.getEvent().getStartTime() == null
                                        || reg.getEvent().getStartTime().isAfter(LocalDateTime.now())));
                    }
                } else {
                    // Offer the list so the user can pick which one to cancel.
                    List<Registration> upcoming = registrationRepository
                            .findUpcomingRegistrationsByUser(user, PageRequest.of(0, 10))
                            .stream()
                            .filter(r -> r.getStatus() != RegistrationStatus.CANCELLED)
                            .toList();
                    result.put("tickets", upcoming.stream().map(this::summarizeTicket).toList());
                    result.put("count", upcoming.size());
                }
            }

            case "ESCALATE" -> {
                // Create a support request capturing the last 10 turns of the
                // conversation so a human agent can pick up with context.
                if (user == null) {
                    result.put("requiresAuth", true);
                    break;
                }
                SupportRequest.Category category = parseCategory((String) intent.get("escalationCategory"));
                List<Map<String, String>> tail = trimTranscript(conversationHistory);
                Event relatedEvent = resolveEventFromContext(sessionContext);
                Registration relatedReg = null;
                UUID relatedRegId = parseUuid(sessionContextGet(sessionContext, "activeRegistrationId"));
                if (relatedRegId != null) {
                    relatedReg = registrationRepository.findById(relatedRegId).orElse(null);
                    if (relatedReg != null && (relatedReg.getUser() == null
                            || !relatedReg.getUser().getId().equals(user.getId()))) {
                        relatedReg = null; // don't leak someone else's ticket
                    }
                }
                SupportRequest created = supportRequestService.escalateFromChat(
                        user,
                        "Chatbot escalation",
                        truncate(userMessage, 2000),
                        category,
                        tail,
                        relatedEvent,
                        relatedReg
                );
                result.put("supportRequestId", created.getId());
                result.put("category", created.getCategory().name());
                result.put("createdAt", created.getCreatedAt() != null
                        ? created.getCreatedAt().toString() : null);
            }

            case "MY_REGISTRATIONS" -> {
                if (user == null) {
                    result.put("authRequired", true);
                    result.put("tickets", List.of());
                    result.put("count", 0);
                } else {
                    List<Registration> regs = registrationRepository
                            .findUpcomingRegistrationsByUser(user, PageRequest.of(0, limit))
                            .getContent();
                    // Return full ticket objects (with registrationId + ticketCode) so
                    // the mobile UI renders interactive _ChatbotTicketCard with QR +
                    // cancel button, not a plain event list.
                    List<Map<String, Object>> tickets = regs.stream()
                            .map(this::summarizeTicket)
                            .toList();
                    result.put("tickets", tickets);
                    result.put("count", tickets.size());
                }
            }

            case "MY_BOOKMARKS" -> {
                if (user == null) {
                    result.put("authRequired", true);
                    result.put("events", List.of());
                    result.put("count", 0);
                } else {
                    List<Event> events = bookmarkRepository.findByUser(user, PageRequest.of(0, limit))
                            .stream().map(Bookmark::getEvent).toList();
                    result.put("events", summarizeEvents(events));
                    result.put("count", events.size());
                }
            }

            case "GREETING", "OFF_TOPIC" -> result.put("info", intentType.toLowerCase());

            default -> result.put("info", "No specific data retrieved for this query type.");
        }

        return result;
    }

    /// User's "preferred" categories: declared `interests` field + categories
    /// of the events they've actually registered for (signal trumps profile).
    private Set<String> preferredCategories(User user) {
        if (user == null) return Set.of();
        Set<String> set = new HashSet<>();

        if (user.getInterests() != null && !user.getInterests().isBlank()) {
            for (String s : user.getInterests().split("[,;]")) {
                String trimmed = s.trim().toLowerCase();
                if (!trimmed.isEmpty()) set.add(trimmed);
            }
        }

        try {
            List<Registration> recent = registrationRepository
                    .findUpcomingRegistrationsByUser(user, PageRequest.of(0, 20))
                    .getContent();
            for (Registration r : recent) {
                Event e = r.getEvent();
                if (e != null && e.getCategory() != null) {
                    set.add(e.getCategory().getName().toLowerCase());
                }
            }
        } catch (Exception ignored) {
        }

        return set;
    }

    private List<Map<String, Object>> summarizeEvents(List<Event> events) {
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm");
        List<Map<String, Object>> list = new ArrayList<>();
        for (Event e : events) {
            Integer capacity = e.getCapacity();
            int approved = e.getApprovedCount();
            // Hide sold-out events from discovery results — nothing the user
            // can do with them, and the bot should never suggest registering
            // for an event that's already full.
            if (capacity != null && capacity > 0 && approved >= capacity) continue;

            Map<String, Object> m = new LinkedHashMap<>();
            m.put("id", e.getId());
            m.put("title", e.getTitle());
            m.put("startTime", e.getStartTime() != null ? e.getStartTime().format(dtf) : null);
            m.put("venue", e.getVenue());
            m.put("city", e.getCity() != null ? e.getCity().getName() : null);
            m.put("category", e.getCategory() != null ? e.getCategory().getName() : null);
            m.put("price", e.getTicketPrice() != null ? e.getTicketPrice() : 0);
            m.put("approvedAttendees", approved);
            m.put("capacity", capacity);
            m.put("spotsRemaining", capacity != null ? Math.max(0, capacity - approved) : null);
            m.put("imageUrl", e.getImageUrl());
            list.add(m);
        }
        return list;
    }

    /// Rich snapshot used by EVENT_DETAILS and COMPARE_EVENTS so the LLM has
    /// enough information to answer follow-ups ("is it free", "what's the
    /// capacity", "where exactly") without another DB round-trip.
    private Map<String, Object> detailedEvent(Event e) {
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm");
        Map<String, Object> m = new LinkedHashMap<>();
        m.put("id", e.getId());
        m.put("title", e.getTitle());
        m.put("description", truncate(e.getDescription(), 600));
        m.put("startTime", e.getStartTime() != null ? e.getStartTime().format(dtf) : null);
        m.put("endTime", e.getEndTime() != null ? e.getEndTime().format(dtf) : null);
        m.put("registrationDeadline", e.getRegistrationDeadline() != null
                ? e.getRegistrationDeadline().format(dtf) : null);
        m.put("venue", e.getVenue());
        m.put("address", e.getAddress());
        m.put("city", e.getCity() != null ? e.getCity().getName() : null);
        m.put("country", e.getCity() != null ? e.getCity().getCountry() : null);
        m.put("category", e.getCategory() != null ? e.getCategory().getName() : null);
        m.put("price", e.getTicketPrice() != null ? e.getTicketPrice() : 0);
        m.put("isFree", e.isFree());
        m.put("capacity", e.getCapacity());
        m.put("approvedAttendees", e.getApprovedCount());
        m.put("organiser", e.getOrganiser() != null ? e.getOrganiser().getFullName() : null);
        m.put("imageUrl", e.getImageUrl());
        return m;
    }

    private String truncate(String s, int max) {
        if (s == null) return null;
        return s.length() <= max ? s : s.substring(0, max) + "…";
    }

    /// Compact projection of TicketType for the BOOK_TICKET card.
    private List<Map<String, Object>> summarizeTicketTypes(List<com.luma.entity.TicketType> ticketTypes) {
        List<Map<String, Object>> list = new ArrayList<>();
        for (com.luma.entity.TicketType t : ticketTypes) {
            int available = Math.max(0, t.getQuantity() - t.getSoldCount());
            Map<String, Object> m = new LinkedHashMap<>();
            m.put("id", t.getId());
            m.put("name", t.getName());
            m.put("description", t.getDescription());
            m.put("price", t.getPrice() != null ? t.getPrice() : java.math.BigDecimal.ZERO);
            m.put("available", available);
            m.put("soldOut", available <= 0);
            m.put("maxPerOrder", t.getMaxPerOrder());
            list.add(m);
        }
        return list;
    }

    /// Small ticket projection used by TICKET_QR and CANCEL_REGISTRATION — we
    /// expose the raw `ticketCode` so the mobile can render a QR locally with
    /// qr_flutter (no extra round-trip to the backend).
    private Map<String, Object> summarizeTicket(Registration r) {
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm");
        Map<String, Object> m = new LinkedHashMap<>();
        m.put("registrationId", r.getId());
        m.put("ticketCode", r.getTicketCode());
        m.put("status", r.getStatus() != null ? r.getStatus().name() : null);
        m.put("checkedIn", r.getCheckedInAt() != null);
        m.put("checkedInAt", r.getCheckedInAt() != null ? r.getCheckedInAt().format(dtf) : null);
        Event e = r.getEvent();
        if (e != null) {
            m.put("eventId", e.getId());
            m.put("eventTitle", e.getTitle());
            m.put("startTime", e.getStartTime() != null ? e.getStartTime().format(dtf) : null);
            m.put("venue", e.getVenue());
            m.put("address", e.getAddress());
            m.put("city", e.getCity() != null ? e.getCity().getName() : null);
            m.put("imageUrl", e.getImageUrl());
        }
        return m;
    }

    private Map<String, Object> summarizeRegistrationPayment(Registration r) {
        Map<String, Object> m = summarizeTicket(r);
        if (r.getUser() != null) {
            try {
                m.put("payment", paymentService.getPaymentByRegistrationId(r.getId(), r.getUser()));
            } catch (Exception ignored) {
                m.put("payment", null);
            }
        }
        return m;
    }

    private UUID parseUuid(Object raw) {
        if (raw == null) return null;
        try { return UUID.fromString(raw.toString()); }
        catch (Exception e) { return null; }
    }

    private Object sessionContextGet(Map<String, Object> ctx, String key) {
        return ctx == null ? null : ctx.get(key);
    }

    private SupportRequest.Category parseCategory(String raw) {
        if (raw == null) return SupportRequest.Category.OTHER;
        try { return SupportRequest.Category.valueOf(raw.trim().toUpperCase()); }
        catch (Exception e) { return SupportRequest.Category.OTHER; }
    }

    /// Keep the last 10 non-empty turns for the support transcript — more
    /// than that is almost never useful and just bloats the database row.
    private List<Map<String, String>> trimTranscript(List<Map<String, String>> history) {
        if (history == null || history.isEmpty()) return List.of();
        int start = Math.max(0, history.size() - 10);
        return history.subList(start, history.size());
    }

    private Event resolveEventFromContext(Map<String, Object> sessionContext) {
        UUID eventId = parseUuid(sessionContextGet(sessionContext, "activeEventId"));
        if (eventId == null) return null;
        return eventRepository.findById(eventId).orElse(null);
    }

    private Event resolveEventForBooking(String keyword, Map<String, Object> sessionContext) {
        if (keyword != null && !keyword.isBlank()) {
            List<Event> match = eventRepository.searchEventsByKeyword(
                    keyword, LocalDateTime.now(), PageRequest.of(0, 1));
            if (!match.isEmpty()) return match.get(0);
        }
        return resolveEventFromContext(sessionContext);
    }

